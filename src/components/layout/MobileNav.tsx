export function MobileNav() {
  const pathname = usePathname();

  return (
    <nav className="lg:hidden fixed bottom-0 left-0 right-0 z-50 glass border-t border-border">
      <div className="flex items-center justify-around px-2 py-2">
        {navItems.map((item) => {
          const isActive =
            pathname === item.href ||
            (item.href !== "/" && pathname.startsWith(item.href));

          if (item.highlight) {
            return (
              <Link
                key={item.href}
                href={item.href}
                className="flex flex-col items-center -mt-6"
              >
                <div
                  className={`w-14 h-14 rounded-2xl flex items-center justify-center transition-all duration-200 ${
                    isActive
                      ? "bg-gradient-to-br from-gold to-gold-dark glow-gold text-navy"
                      : "bg-gradient-to-br from-gold/80 to-gold-dark/80 text-navy"
                  }`}
                >
                  {item.icon}
                </div>
                <span
                  className={`text-[10px] mt-1 font-medium ${
                    isActive ? "text-gold" : "text-text-muted"
                  }`}
                >
                  {item.label}
                </span>
              </Link>
            );
          }

          return (
            <Link
              key={item.href}
              href={item.href}
              className="flex flex-col items-center py-2 px-3"
            >
              <span
                className={`transition-colors ${
                  isActive ? "text-gold" : "text-text-dim"
                }`}
              >
                {item.icon}
              </span>
              <span
                className={`text-[10px] mt-1 font-medium ${
                  isActive ? "text-gold" : "text-text-muted"
                }`}
              >
                {item.label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
